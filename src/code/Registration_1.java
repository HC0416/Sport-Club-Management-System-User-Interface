    private void Input_FNKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Input_FNKeyReleased
        // TODO add your handling code here:
        String PATTERN= "(?i)[a-z]([- ',.a-z]{0,23}[a-z])?";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(Input_FN.getText());
        if(!match.matches()){
            lName_Valid.setText("*Can only contain alphabet!");
            lName_valid=false;
        }
        else{
            lName_Valid.setText(null);
            lName_valid=true;
        }
    }//GEN-LAST:event_Input_FNKeyReleased

    private void Input_PhoneKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Input_PhoneKeyReleased
        // TODO add your handling code here:
        String PATTERN= "^\\d{10}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(Input_Phone.getText());
        if(!match.matches()){
            PhoneN_Valid.setText("*Can only contain numbers!");
            pN_valid=false;
        }
        else{
            PhoneN_Valid.setText(null);
            pN_valid=true;
        }
        
      
    }//GEN-LAST:event_Input_PhoneKeyReleased

    private void Input_EmailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Input_EmailKeyReleased
        // TODO add your handling code here:
        String PATTERN= "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(Input_Email.getText());
        if(!match.matches()){
            Email_Valid.setText("*Email is invalid");
            email_valid=false;
        }
        else{
            Email_Valid.setText(null);
            email_valid=true;
        }
    }//GEN-LAST:event_Input_EmailKeyReleased

