    private void Next_pageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Next_pageActionPerformed
        // TODO add your handling code here:
        int count = 0;
        if(!Input_FN.getText().isEmpty()){
            count = count + 1;
        }
        if(!Input_Email.getText().isEmpty()){
            count = count + 1;
        }
        if(!Input_Phone.getText().isEmpty()){
            count = count + 1;
        }
        if(!Input_Password.getText().isEmpty()){
            count = count + 1;
        }
        if(!Input_CoPassword.getText().isEmpty()){
            count = count + 1;
        }
        if(count == 5){
            String main_pw = Input_Password.getText();
            String co_pw = Input_CoPassword.getText();
            if(main_pw.equals(co_pw)){
                Students student = new Students();   
                student.set_name(Input_FN.getText());
                student.set_email(Input_Email.getText());
                student.set_phone(Input_Phone.getText());
                student.set_password(main_pw);
                Registration_2 r2 = new Registration_2(student);
                r2.setVisible(true);
                this.setVisible(false);
            }
            else{
                showMessageDialog(null, "Password not match");
            }
        }else{
            showMessageDialog(null, "Please fill in all the fill before proceeding to the next page");
        }

    }//GEN-LAST:event_Next_pageActionPerformed

