    private void addNewCoachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewCoachActionPerformed
        // TODO add your handling code here:
        int i = 0;
        if(!coachName.getText().isEmpty()){
            coach.set_name(coachName.getText());
            i = i+1;
        }
        if(!joinDate.getText().isEmpty()){
            coach.set_jdate(joinDate.getText());
            i = i + 1;
        }
        if(!hourlyRate.getText().isEmpty()){
            try{
                coach.set_h_rate(Double.parseDouble(hourlyRate.getText()));
                i = i + 1;
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Register unsuccessful, please enter a valid double value in the hourly rate field");
            }
        }
        if(!phoneNo.getText().isEmpty()){
            coach.set_phone(phoneNo.getText());
            i = i + 1;
        }
        if(!buildingNo.getText().isEmpty()){
            i = i + 1;
        }
        if(!streetName.getText().isEmpty()){
            i = i + 1;
        }
        if(!cityName.getText().isEmpty()){
            i = i + 1;
        }
        if(!zipNo.getText().isEmpty()){
            i = i + 1;
        }
        if(!stateName.getText().isEmpty()){
            i = i + 1;
        }
        if(!country.getText().isEmpty()){
            i = i + 1;
        }
        if(selectSport.getSelectedItem() != null){
            String[] array1 = sport.readid_to_array(admin.get_c_code());
            int idx = selectSport.getSelectedIndex();
            coach.set_s_code(array1[idx]);
            i = i + 1;
        }
        
        if(i == 11){
            coach.set_address(buildingNo.getText(),streetName.getText(),cityName.getText(),zipNo.getText(),stateName.getText(),country.getText());
            coach.set_c_code(admin.get_c_code());
            coach.add_coach();
            coachName.setText("");
            joinDate.setText("");
            hourlyRate.setText("");
            phoneNo.setText("");
            buildingNo.setText("");
            streetName.setText("");
            cityName.setText("");
            zipNo.setText("");
            stateName.setText("");
            country.setText("");
            selectSport.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "New coach added successful");
        }else{
            JOptionPane.showMessageDialog(null, "Register unsuccessful, please fill in all fields");
        }
    }//GEN-LAST:event_addNewCoachActionPerformed

