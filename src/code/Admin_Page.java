    private void modify_scheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modify_scheduleActionPerformed
        // TODO add your handling code here:
        String[] day = new String[]{"Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"};
        String[] AvailableScheduleID = schedule.readid_to_array();
        int count = 0;
        if(selectScheduleID.getSelectedItem()!= null){
            count = count + 1;
        }
        if(!startChange.getText().isEmpty()&!endChange.getText().isEmpty()){
            count = count + 1;
        }
        if(dayChange.getSelectedItem()!=null){
            count = count + 1;
        }
        if(count==3){
            int ASIDidx = selectScheduleID.getSelectedIndex();
            schedule.set_schedule_id(AvailableScheduleID[ASIDidx]);
            int didx = dayChange.getSelectedIndex();
            try {
                schedule.update_schedule(day[didx],startChange.getText(), endChange.getText());
            } catch (IOException ex) {
                Logger.getLogger(Admin_Page.class.getName()).log(Level.SEVERE, null, ex);
            }
            String[] AvailableScheduleTime = schedule.readtime_to_array();
            selectScheduleID.setSelectedIndex(-1);
            dayChange.setSelectedIndex(-1);
            endChange.setText("");
            startChange.setText("");
            scheduleChange.removeAllItems();
            for(int i=0; i<AvailableScheduleTime.length;i++){
                scheduleChange.insertItemAt(AvailableScheduleTime[i], i);
            }
            JOptionPane.showMessageDialog(null, "Update Successful");
        }else{
            JOptionPane.showMessageDialog(null, "Update failed, please fill in all fields");
        }
        
    }//GEN-LAST:event_modify_scheduleActionPerformed

